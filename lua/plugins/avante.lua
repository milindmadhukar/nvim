function M.config()
  require("avante").setup({
    provider = "claude",
    claude = {
      endpoint = "https://api.anthropic.com",
      model = "claude-3-5-sonnet-latest",
      temperature = 0,
      max_tokens = 4096,
    },
    mappings = {
      ask = "<leader>Aa",
      edit = "<leader>Ae",
      refresh = "<leader>Ar",
      --- @class AvanteConflictMappings
      diff = {
        ours = "co",
        theirs = "ct",
        both = "cb",
        next = "]x",
        prev = "[x",
      },
      jump = {
        next = "<C-n>",
        prev = "<C-p>",
      },
      submit = {
        normal = "<CR>",
        insert = "<C-s>",
      },
      toggle = {
        debug = "<leader>Ad",
        hint = "<leader>Ah",
      },
    },
  })
end

return M
